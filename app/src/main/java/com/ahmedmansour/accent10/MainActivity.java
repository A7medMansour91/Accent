    //copy database
    private boolean copyDatabase(Context context) {
        try {

            InputStream inputStream = context.getAssets().open(DBAccent.DBNAME);
            String outFileName = DBAccent.DBLOCATION + DBAccent.DBNAME;
            OutputStream outputStream = new FileOutputStream(outFileName);
            byte[]buff = new byte[1024];
            int length = 0;
            while ((length = inputStream.read(buff)) > 0) {
                outputStream.write(buff, 0, length);
            }
            outputStream.flush();
            outputStream.close();
            Log.w("MainActivity","DB copied");
            return true;
        }catch (Exception e) {
            e.printStackTrace();
            return false;
        }
    }

